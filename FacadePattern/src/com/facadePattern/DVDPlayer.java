package com.facadePattern;

public class DVDPlayer {
	public void on() {
		System.out.println("DVD player is on.");
	}
	
	public void off() {
		System.out.println("DVD player is off.");
	}
	
	public void play() {
		System.out.println("DVD player is playing.");
	}
	
	public void stop() {
		System.out.println("DVD player stopped.");
	}
}
