package com.facadePattern;

public class Screen {
	public void on() {
		System.out.println("Screen is on.");
	}
	
	public void off() {
		System.out.println("Screen is off.");
	}
	
	public void up() {
		System.out.println("Screen is up.");
	}
	
	public void down() {
		System.out.println("Screen is down.");
	}
}
